package com.fifa.entity;

import java.util.Date;

public class Country {
    
    private String name;
    
    private String rank;
    
    private String points;
    
    private String flag;
    
    private String category;
    
    private String countryLink;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the rank
     */
    public String getRank() {
        return rank;
    }

    /**
     * @param rank the rank to set
     */
    public void setRank(String rank) {
        this.rank = rank;
    }

    /**
     * @return the points
     */
    public String getPoints() {
        return points;
    }

    /**
     * @param points the points to set
     */
    public void setPoints(String points) {
        this.points = points;
    }

    /**
     * @return the flag
     */
    public String getFlag() {
        return flag;
    }

    /**
     * @param flag the flag to set
     */
    public void setFlag(String flag) {
        this.flag = flag;
    }

    /**
     * @return the category
     */
    public String getCategory() {
        return category;
    }

    /**
     * @param category the category to set
     */
    public void setCategory(String category) {
        this.category = category;
    }

    /**
     * @return the countryLink
     */
    public String getCountryLink() {
        return countryLink;
    }

    /**
     * @param countryLink the countryLink to set
     */
    public void setCountryLink(String countryLink) {
        this.countryLink = countryLink;
    }

   
    

}
