package com.jn.kickoff.holder;

public class News {
	private String newsheading;
	private String detailednews;
	private String newsimage;
	private String source;
	private String newsdate;
	/**
	 * @return the newsheading
	 */
	public String getNewsheading() {
		return newsheading;
	}
	/**
	 * @param newsheading the newsheading to set
	 */
	public void setNewsheading(String newsheading) {
		this.newsheading = newsheading;
	}
	/**
	 * @return the detailednews
	 */
	public String getDetailednews() {
		return detailednews;
	}
	/**
	 * @param detailednews the detailednews to set
	 */
	public void setDetailednews(String detailednews) {
		this.detailednews = detailednews;
	}
	/**
	 * @return the newsimage
	 */
	public String getNewsimage() {
		return newsimage;
	}
	/**
	 * @param newsimage the newsimage to set
	 */
	public void setNewsimage(String newsimage) {
		this.newsimage = newsimage;
	}
	/**
	 * @return the source
	 */
	public String getSource() {
		return source;
	}
	/**
	 * @param source the source to set
	 */
	public void setSource(String source) {
		this.source = source;
	}
	/**
	 * @return the newsdate
	 */
	public String getNewsdate() {
		return newsdate;
	}
	/**
	 * @param newsdate the newsdate to set
	 */
	public void setNewsdate(String newsdate) {
		this.newsdate = newsdate;
	}
	

}
